const a = 7; // = atribuição
let b = 3;

b += a ; // Igual em java(valor atual mais o valor passado)
console.log(b);

b -= 4 ; // Igual em java(valor atual menos o valor passado)
console.log(b);

b *= 2 ; // Igual em java(valor atual multiplicar o valor passado)
console.log(b);

b /= 2 ; // Igual em java(valor atual dividir o valor passado)
console.log(b);

b %= 2 ; // Igual em java(modulo do valor atual)
console.log(b);